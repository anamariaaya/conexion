<h2 class="pGreen">Hospitales, clínicas, otros centros médicos en áreas comunes Y consultorios</p>

<p>Mantenemos  a nuestros operarios bien capacitados para que desarrollen
sus labores específicas en esta área, en el manejo de productos químicos peligrosos, ruta sanitaria,
clasificación de residuos y el manejo del código de colores, caracterizados siempre con una gran
atención al cliente.</p>

<p>Nuestros colaboradores reciben constante actualización en manejo y clasificación de  residuos de acuerdo al grado de peligrosidad en infecciones o riesgo biológico.</p>

<p>Las capacitaciones consisten en
el manejo del código de colores, ayudando a distinguir las zonas críticas y los puntos de control HACCP, con el objetivo de evitar la contaminación cruzada al clasificar los residuos con una adecuada segregación de los mismos en la fuente de su generación.
Con los protocolos de aseo y desinfección que manejamos buscamos reducir o eliminar el número
de microorganismos presentes en las áreas, superficies u objetos para disminuir los riesgos de
infección.</p>

